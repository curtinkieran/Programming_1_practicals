length = float(input('Enter a length: '))
import math
area_square = length**2
volume_cube = length**3
radius = length/2
area_circle = (math.pi*radius)**2
volume_sphere = (4/3)*(math.pi*radius**3)
volume_cylinder = (math.pi)*(radius**2)*length
print (area_square)
print (volume_cube)
print (area_circle)
print (volume_sphere)
print (volume_cylinder)

             
